import React, { useState, useEffect } from 'react';
import {
  View,
  Text,
  StyleSheet,
  FlatList,
  TouchableOpacity,
  RefreshControl,
  Alert,
} from 'react-native';
import { theme } from '../theme';
import { fetchSharedTokens, cancelSharedToken } from '../api';

export const SharedTokensScreen = () => {
  const [tokens, setTokens] = useState([]);
  const [loading, setLoading] = useState(false);
  const [refreshing, setRefreshing] = useState(false);

  useEffect(() => {
    loadTokens();
  }, []);

  const loadTokens = async () => {
    setLoading(true);
    const result = await fetchSharedTokens();
    setLoading(false);

    if (result.success) {
      setTokens(result.data);
    } else {
      // Quietly fail or show empty
      setTokens([]);
    }
  };

  const onRefresh = async () => {
    setRefreshing(true);
    const result = await fetchSharedTokens();
    setRefreshing(false);
    if (result.success) {
      setTokens(result.data);
    }
  };

  const handleCancel = token => {
    Alert.alert(
      'Confirm Cancellation',
      'Are you sure you want to remove this shared token?',
      [
        { text: 'No', style: 'cancel' },
        {
          text: 'Yes',
          style: 'destructive',
          onPress: async () => {
            const result = await cancelSharedToken(token.user_token_shared_id);
            if (result.success) {
              Alert.alert('Success', result.message);
              loadTokens();
            } else {
              Alert.alert('Error', result.message);
            }
          },
        },
      ],
    );
  };

  const renderTokenItem = ({ item }) => (
    <View style={styles.card}>
      <View style={styles.header}>
        <Text style={styles.companyName}>{item.company_name}</Text>
        <View
          style={[
            styles.statusBadge,
            item.token_status === 'I'
              ? styles.statusActive
              : styles.statusInactive,
          ]}
        >
          <Text style={styles.statusText}>
            {item.token_status === 'I' ? 'Active' : 'Expired'}
          </Text>
        </View>
      </View>

      <Text style={styles.queueName}>{item.queue_name}</Text>

      <View style={styles.detailsRow}>
        <View style={styles.detailItem}>
          <Text style={styles.detailLabel}>Token No</Text>
          <Text style={styles.detailValue}>{item.token_no}</Text>
        </View>
        <View style={styles.detailItem}>
          <Text style={styles.detailLabel}>Date</Text>
          <Text style={styles.detailValue}>{item.queue_date}</Text>
        </View>
        <View style={styles.detailItem}>
          <Text style={styles.detailLabel}>Waiting</Text>
          <Text style={styles.detailValue}>{item.current_waiting}</Text>
        </View>
      </View>

      <Text style={styles.sharedBy}>
        Shared by: {item.shared_by_name || 'Unknown'}
      </Text>

      {item.token_status === 'I' && (
        <View style={styles.footer}>
          <TouchableOpacity
            style={styles.cancelButton}
            onPress={() => handleCancel(item)}
          >
            <Text style={styles.cancelButtonText}>Remove Token</Text>
          </TouchableOpacity>
        </View>
      )}
    </View>
  );

  return (
    <View style={styles.container}>
      <FlatList
        data={tokens}
        renderItem={renderTokenItem}
        keyExtractor={item => item.user_token_shared_id}
        contentContainerStyle={styles.listContent}
        refreshControl={
          <RefreshControl refreshing={refreshing} onRefresh={onRefresh} />
        }
        ListEmptyComponent={
          <View style={styles.emptyContainer}>
            <Text style={styles.emptyIcon}>üéüÔ∏è</Text>
            <Text style={styles.emptyText}>No shared tokens found</Text>
            <Text style={styles.emptySubtext}>
              Tokens shared with you will appear here
            </Text>
          </View>
        }
      />
    </View>
  );
};

